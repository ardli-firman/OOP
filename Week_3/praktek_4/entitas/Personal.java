package entitas;

/**
 * Personal
 */
public class Personal {

    private String nik;
    private String nama;

    public Personal() {

    }

    public String getNik() {
        return this.nik;
    }

    public void setNik(String nik) {
        this.nik = nik;
    }

    public String getNama() {
        return this.nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }
}
